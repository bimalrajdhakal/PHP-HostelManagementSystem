                <h3>Hostel Rules</h3>
                <strong>1.0.0 Admission</strong>
					<p> 
						<ol type="circle">
							<li>Admission to the University Hostels shall be open mainly to full time regular students admitted to a course of study of not less than two semesters.  Full time research students and fellows working in a sponsored research schemes in the University shall only be considered if seats are available.  Full time students under <b>Faculty Improvement Programme</b> drawing regular salary for their parent institutions shall not be eligible for hostel accommodation.</li>
							<li>Admission to Hostels is not the right of any student.  The Syndicate reserves the right to open or close any or all of the hostels, or parts thereof deny admission to or expel any student, or shift a student from one hostel to another all without assigning any reason.  No Ph. D. student shall be allowed to stay in hostel for a total or more than four years in his/her academic career in the University. </li>
							<li>During vacation the hostels shall remain closed.  Admission to hostels shall be made every academic year.  The students shall vacate the hostels during vacation and they shall remove their personal belongings at the time of vacating the hostel during vacation. </li>
							<li>Application for admission to the hostel shall be made in the prescribed form (Form A).</li>
							<li>Admissions to hostels are made by a committee consisting of the Hon. Warden and the Registrar on the recommendation of the Head of Departments, normally at the beginning of the course or research work or after a vacation.  While recommending accommodation to the Head of the Department will ensure that priority is given to those students whose normal place of residence not less than 40 kilometers away from the campus, except in the case of SC/ST and physically handicapped students.</li>
							<li>Priority in admission to hostel shall be given to SC/ST and physically handicapped students and foreign students. </li>
							<li>Accommodation in the hostel shall be limited to the duration of the course for which a student is admitted and he/she shall be required to vacate the hostel within 7 days of his /her taking the last written/practical/viva voce examination of the regular course. One is not eligible for accommodation in the hostel for taking supplementary examination or viva voce examination.</li>
							<li>A student admitted to an M.Phil. programme shall not be allowed accommodation beyond a period of 12 months from the date of admission to the hostel. </li>
							<li>A student admitted to research for Ph.D shall be allowed accommodation only for a period of three years (for the first two years in a double seated room and after that eligible for a single room subject to availability).  However, in the event of a scholar submitting the thesis earlier, he/she leave the hostel immediately. </li>
							<li>If an inmate of a hostel after completing the full time course gets himself/herself enrolled for another full time course, his/her application for admission to the hostel shall be treated as a fresh one and he/she shall not be entitled to any preference by virtue of his/her earlier residence in the hostel. </li>
							<li>Fees payments to hostel at the time of fresh admission as shown in Sec. 4 shall be payable in the concerned Departments on production of Form A duly endorsed by the Hon. Warden and the Registrar.  All subsequent payments of hostel dues shall be made monthly in the concerned Departments.  The Head of the Department may consolidate such receipts and the details be furnished to the Hon. Warden. On surrendering the hostel fee receipt in the hostel office and the submitting of Form A duly filled in, the student may be allotted a seat by the Hon. Warden.  On the allotment of seat, the student may report to the Assistant Warden/Matron is the case may be, who will finally allot proper room in the concerned hostel.</li>
							<li>On allotting the room, the student has to fill in Form B a declaration, stating that he/she is jointly and severally responsible for the furniture and fixtures in the room and the hostel. </li>
							<li>Students admitted to the hostel shall not engage themselves in any unlawful act, destruction of public property, or in any other act of indiscipline. </li>
							<li>The Heads of Departments shall maintain a separate list of students recommended for hostel accommodation and inform the Hostel Office the last date of the written/practical/viva voce examinations of the final semester regular course, at least 15 days in advance.  And students shall be permitted to take the examination only on the production of NO DUES CERTIFICATE from the hostel, each semester. In the Final semester, they shall be required to fill in the Form C the application for refund of caution deposit.  And obtain the Final No Dues Certificate from the hostel.</li>
							<li>Key of the room should not be transferred by the hosteller to another person.  If a room is found closed for a week without intimation to the Warden, the hosteller will be expelled from the hostel. </li>
						</ol>
					</p>
					
                <strong>2.00. Study Time </strong>
					<p> 
						<ol type="circle">
							<li>Silence should be observed during the study time which will be decided by the Hon. Warden. </li>
							<li>Inmates of Boys Hostels are expected to be back in their room not later than  9.00 pm each day. Those who return to the hostel after 9.00 p.m. shall enter their names and room numbers in the LATE COMERS Register maintained in the Hostel and state the reason for delay. Research Scholars may be permitted to stay late in the Library/Laboratory with the specific sanction of the Wardens concerned.</li>
							<li>Discipline in the Ladies Hostel</li>
								<ol>
							         <li>Visitors will be allotted in the Ladies hostel only upto 6.30 p.m. Visitors in the Ladies Hostel must be received only in the visitors room.  They are not allowed to be taken in the rooms. </li>
									 <li>The inmates should return to their rooms before 7 pm</li>
									 <li>Research scholars who have to do experimental work must return by 9.00 pm</li>
									 <li>Silence should be observed in the hostel premises from 9.00 p.m. onwards in order to create an atmosphere conducive for study. </li>
								</ol>
						</ol>
					</p>
                <strong>3.00 Visitors and Guests </strong>
                <p>
					<ol>
					     <li>Visitors shall not be entertained in the rooms during the study time and at night.  </li>
						 <li>No guests will be allowed to stay in the hostel except in Ladies Hostels where special permission has to be obtained from the Registrar for this purpose.  Guest charges will be Rs. 25/- per day for students of Cochin University, Rs.50/- for outside University students and Rs.100/- for parents of Cochin University students.  Strict disciplinary action will be taken against inmates of hotels accommodating guests unauthorisedly. </li>
					</ol>
				</p>
                <strong>4.00 Fees and Charges </strong>
                <p>
					<ol>
					     <li>Caution Deposit              : Rs. 500.00</li>
						 <li>Admission fee                 : Rs. 100.00</li>
						 <li>Association Fee               : Rs. 10.00 per semester</li>
						 <li>Room Rent</li>
						    <ol>
								<li>Rs.250.00 per head per month for foreign students </li>
								<li>Rs.40.00  per head per month for multi seated room </li>
							</ol>
						 <li>Establishment charges of Rs.40/- per head per month (common to all hostels inmates).</li>
						 <li>Water charges Rs. 10/- per head per month.</li>
						 <li>Electricity charges 20/ as per consumption (Hostel wise) will be divided by the students along with mess charges.
							 Note. 4.01 to 4.07 to be collected by the Departments and credited to Cochin University fund.   The hostel fee shall be collected without fine till the 10th of the month.  Fine of Rs.10/- be levied upto the 20th day of the month.  Fine of Rs.10/- and Rs.2/- per day be collected beyond 20 days and before 30 days.  After 30 days Rs.100/- shall be charged as readmission fee in addition to the fine rates. (U.O. No. Ac.A2/3431/91/PF(I) dated 29.9.2008)</li>
					</ol>
				</p>
				<strong>5.00 Mess </strong>
						<p>
							<ol>
								<li>Inmates of the hostels may run a mess on a dividing basis, by electing a Mess Secretary, a mess committee and an audit committee from among themselves from time to time under the overall supervision of the Warden. Mess staff will be paid by the students and the University will not be responsible for any liability thereon. </li>
								<li>The Mess committee is responsible for the safe keeping of the articles supplied by the University for the running of the mess. </li>
							</ol>
						</p>
				<strong>6.00 Recreation facilities </strong>
						<p>
							<ol>
								<li>Limited facilities for indoor and outdoor games are provided and a three newspapers are subscribed to by the University.</li>
							</ol>
						</p>
				<strong>7.00 House keeping Committee  </strong>
                <p>
							<ol>
								<li>A house keeping committee of the inmates under the leadership of the Hostel Secretary, elected from time to time may ensure clean physical environment and discipline among the inmates.</li>
							</ol>
				</p>
				<strong>8.00 Vacating the Hostel </strong>
                <p>
							<ol>
								<li>The students should complete the Form C and submit the same to the Hostel Office, at least 15 days before the date of vacating the Hostel.</li>
								<li>Before receiving the Caution Deposit back, the student should complete the Form B, obtain orders from the Hon. Warden and submit the same to their respective departments.</li>
								<li>Hostellers are to vacate the hostels during the annual vacation or as and when asked to do so by the University. </li>
								<li>Fresh admissions will be made to the hostels at the beginning of the academic year or even during the course of an academic year at the discretion of the University. </li>
								<li>Transfers between rooms or hostels are not permitted, but in special cases  requests based on specific reasons may be considered by the Assistant Warden, Hon. Warden and Registrar of the University on merits. </li>
								<li>Subject to the overall control of the University the Hon. Warden and Registrar together shall have the authority to transfer a student from one room to another room or from one hostel to another hostel without assigning any reasons. </li>
								<li>The Head of the Department shall intimate the Hostel Office, at least 15 days in advance the names of students who are to be sent out of station for field work or practical training. The resident shall intimate the Warden in writing the period of his /her absence for training or field work.  During such period, the room shall not be occupied by any other person. If the Warden is not available the resident shall inform (in writing) the Hostel Office the period of absence.</li>
								<li>Anyone leaving without prior intimation shall be treated as having vacated the hostel without authorisation, and readmission may be made only on the written recommendation of the Head of the Department. </li>
								<li>Defaulters of hotel dues for more than two months, shall be removed from the hostel.</li>
							</ol>
				</p>
          
